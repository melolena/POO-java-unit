package exercicio1;

public class Calculadora {
	int operando1;
	 int operando2;
	 int adicao() {
	 return operando1 + operando2;
	 }
	 int subtracao() {
	 return operando1 - operando2;
	 }
	 int multiplicacao() {
	 return operando1 * operando2;
	 }
	 int divisao() {
	 return operando1 / operando2;
	 }
}
